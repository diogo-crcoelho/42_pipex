char	*get_path(char **envp, char *str)
{
	int	i;

	i = 0;
	while (*envp && i < 3)
	{
		i = -1;
		while (++i < 4)
			if ((*envp)[i] != str[i])
				break ;
		envp += (i != 4);
	}
	return (&(*envp)[5]);
}
